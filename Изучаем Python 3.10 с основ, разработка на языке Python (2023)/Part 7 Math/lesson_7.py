#Математика
import math
#Возведение числа в степень
n1 = math.pow(2,4)
print(n1)
n2 = 2**4
print(n2)

#Квадратный корень числа
n3 = math.sqrt(16)
print(n3)

#Ближайшее целое большее число
n4 = math.ceil(8.56)
print(n4)

#Ближайшее целое наименьшее число
n5 = math.floor(8.56)
print(n5)

#Перевод из радиан в градусы
n6 = math.degrees(8.56)
print(n6)